
export const sum = (num1: number, num2: number) => {
    return num1 + num2;
};

export const difference = (num1: number, num2: number) => {  
    return num1 - num2;
};

export const product = (num1: number, num2: number) => {  
    return num1 * num2;
}

export const quotient = (num1: number, num2: number) => {  
    return num1 / num2;
}